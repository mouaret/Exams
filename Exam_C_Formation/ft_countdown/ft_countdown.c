#include <unistd.h>

void	ft_putchar2(char c)
{
	write(1, &c, 1);
}

int		main()
{
	char c;

	c = '9';
	while (c >= '0')
		ft_putchar2(c--);
	ft_putchar2('\n');
	return (0);
}
